
import { Request, Response } from "express";
import { AuditService } from "../services/AuditService";

class AuditController {

    // For testing purposes - in a real app this would be internal or triggered by business logic
    async createLog(req: Request, res: Response) {
        try {
            const { userId, formId, actionType, changes } = req.body;
            if (!userId || !formId || !actionType || !changes) {
                return res.status(400).json({ error: "Campos obrigatórios não informados" });
            }

            const auditService = new AuditService();
            const log = await auditService.logTransaction(userId, formId, actionType, changes);
            return res.status(201).json(log);
        } catch (error: any) {
            console.error(error);
            return res.status(500).json({ error: error.message });
        }
    }

    async revertLog(req: Request, res: Response) {
        try {
            const { auditLogId } = req.body;
            if (!auditLogId) {
                return res.status(400).json({ error: "auditLogId é obrigatório" });
            }

            const auditService = new AuditService();
            const result = await auditService.revertTransaction(auditLogId);
            return res.status(200).json(result);
        } catch (error: any) {
            console.error(error);
            return res.status(500).json({ error: error.message });
        }
    }
}

export { AuditController };
