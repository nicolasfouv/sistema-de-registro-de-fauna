import { useState } from "react";
import type { User } from "./users";
import { deleteUser } from "../../services/userService";


interface DeleteUserModalProps {
    user: User,
    close: () => void,
    refresh: () => void
}

export function DeleteUserModal({ user, close, refresh }: DeleteUserModalProps) {
    const [loading, setLoading] = useState(false);

    async function handleSubmitDelete(e: React.FormEvent<HTMLFormElement>) {
        e.preventDefault();
        setLoading(true);
        try {
            await deleteUser(user.id);
            refresh();
            close();
        } catch (error) {
            console.error(error);
        } finally {
            setLoading(false);
        }
    }

    return (
        <div className="flex justify-center items-center fixed top-0 left-0 w-full h-full bg-black/50 z-100">
            <div className="relative flex flex-col bg-white w-160 justify-center items-center rounded-2xl shadow-xl p-10">

                <button
                    onClick={() => close()}
                    className="absolute cursor-pointer bg-standard-blue w-10 h-10 rounded-xl top-2 right-2 text-white text-xl font-bold flex items-center justify-center"
                >
                    ✕
                </button>

                <h2 className="text-2xl text-standard-blue font-bold -mt-6 mb-6">
                    Confirmação de Exclusão
                </h2>

                <form onSubmit={(e) => handleSubmitDelete(e)} className="w-full h-full flex flex-col items-center justify-center">
                    <p className="text-xl text-center">Deseja realmente excluir o usuário <span className="font-bold">{user.name}</span> ?</p>

                    <div className="flex justify-center items-center gap-5 mt-4">
                        <button
                            type="submit"
                            className="bg-standard-blue text-white text-xl font-bold py-2 px-5 rounded-xl cursor-pointer"
                            disabled={loading}
                        >
                            {loading ? 'Confirmando...' : 'Confirmar'}
                        </button>
                        <button
                            onClick={() => close()}
                            className="bg-standard-blue text-white text-xl font-bold py-2 px-5 rounded-xl cursor-pointer"
                        >
                            Cancelar
                        </button>
                    </div>
                </form>
            </div>
        </div>
    )

}